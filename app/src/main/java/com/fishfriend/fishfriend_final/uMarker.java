package com.fishfriend.fishfriend_final;

public class uMarker {
        public String username;
        public double lat;
        public double lon;
        public String date;

        public uMarker() { }

        public uMarker(String username, double lat, double lon, String date) {
            this.username = username;
            this.lat = lat;
            this.lon = lon;
            this.date = date;
        }
    }